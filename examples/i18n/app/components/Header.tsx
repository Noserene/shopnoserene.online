import {Await, useMatches} from '@remix-run/react';
import {Suspense} from 'react';
import type {LayoutProps} from './Layout';
import {LocaleSwitcher} from '~/components/LocaleSwitcher';
import {LocalizedLink} from '~/components/LocalizedLink';
import {useTranslation} from 'react-i18next';

type HeaderProps = Pick<LayoutProps, 'header' | 'cart' | 'isLoggedIn'>;

type Viewport = 'desktop' | 'mobile';

export function Header({header, isLoggedIn, cart}: HeaderProps) {
  const {shop, menu} = header;
  return (
    <header className="header">
      <LocalizedLink prefetch="intent" to="/">
        <strong>{shop.name}</strong>
      </LocalizedLink>
      <HeaderMenu menu={menu} viewport="desktop" />
      <HeaderCtas isLoggedIn={isLoggedIn} cart={cart} />
    </header>
  );
}

export function HeaderMenu({
  menu,
  viewport,
}: {
  menu: HeaderProps['header']['menu'];
  viewport: Viewport;
}) {
  const [root] = useMatches();
  const publicStoreDomain = root?.data?.publicStoreDomain;
  const className = `header-menu-${viewport}`;

  function closeAside(event: React.MouseEvent<HTMLAnchorElement>) {
    if (viewport === 'mobile') {
      event.preventDefault();
      window.location.href = event.currentTarget.href;
    }
  }

  return (
    <nav className={className} role="navigation">
      {viewport === 'mobile' && (
        <LocalizedLink prefetch="intent" to="/" onClick={closeAside}>
          Home
        </LocalizedLink>
      )}
      {(menu || FALLBACK_HEADER_MENU).items.map((item) => {
        if (!item.url) return null;

        // if the url is internal, we strip the domain
        const url =
          item.url.includes('myshopify.com') ||
          item.url.includes(publicStoreDomain)
            ? new URL(item.url).pathname
            : item.url;
        return (
          <LocalizedLink
            key={item.id}
            prefetch="intent"
            className="header-menu-item"
            to={url}
            onClick={closeAside}
          >
            {item.title}
          </LocalizedLink>
        );
      })}
    </nav>
  );
}

function HeaderCtas({
  isLoggedIn,
  cart,
}: Pick<HeaderProps, 'isLoggedIn' | 'cart'>) {
  const {t} = useTranslation();
  return (
    <nav className="header-ctas" role="navigation">
      <LocaleSwitcher />
      <HeaderMenuMobileToggle />
      <LocalizedLink prefetch="intent" to="/account">
        {isLoggedIn
          ? t('layout.header.ctas.account')
          : t('layout.header.ctas.login')}
      </LocalizedLink>
      <SearchToggle />
      <CartToggle cart={cart} />
    </nav>
  );
}

function HeaderMenuMobileToggle() {
  return (
    <a className="header-menu-mobile-toggle" href="#mobile-menu-aside">
      <h3>â˜°</h3>
    </a>
  );
}

function SearchToggle() {
  const {t} = useTranslation();
  return <a href="#search-aside">{t('layout.header.ctas.search')}</a>;
}

function CartBadge({count}: {count: number}) {
  const {t} = useTranslation();
  return (
    <a href="#cart-aside">
      {t('layout.header.ctas.cart')} {count}
    </a>
  );
}

function CartToggle({cart}: Pick<HeaderProps, 'cart'>) {
  return (
    <Suspense fallback={<CartBadge count={0} />}>
      <Await resolve={cart}>
        {(cart) => {
          if (!cart) return <CartBadge count={0} />;
          return <CartBadge count={cart.totalQuantity || 0} />;
        }}
      </Await>
    </Suspense>
  );
}

